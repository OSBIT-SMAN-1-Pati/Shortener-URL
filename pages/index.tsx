import { NextPage } from "next";
import Head from "next/head";
import {  useFormik } from "formik";
import getMetaTags from "../utils/extractMetaTag";


const Index: NextPage = () => {
  const formik = useFormik({
    initialValues: {
      firstName: "",

      lastName: "",

      email: "",
    },

    onSubmit: async () => {
      const res = await fetch("/api/metascrap")
      const {msg} = await res.json()
      alert(JSON.stringify(msg))
    },
  });
  return (
    <>
      <Head>
        <title>shortener-url</title>
        <meta name="description" content="Generated by Create Next Stack." />
      </Head>
      <h1>Welcome to OSBIT Shortener URL</h1>
      <p>Try changes the url to see what will happen</p>
      <p>URL:</p>
      <p>1. /Youtube</p>
      <p>2. /instagaram</p>
      <p>3. /facebook</p>
      <p>{formik.values.firstName}</p>
      <form onSubmit={formik.handleSubmit}>
        <label htmlFor="firstName">First Name</label>

        <input
          id="firstName"
          name="firstName"
          type="text"
          onChange={formik.handleChange}
          value={formik.values.firstName}
        />

        <label htmlFor="lastName">Last Name</label>

        <input
          id="lastName"
          name="lastName"
          type="text"
          onChange={formik.handleChange}
          value={formik.values.lastName}
        />

        <label htmlFor="email">Email Address</label>

        <input
          id="email"
          name="email"
          type="email"
          onChange={formik.handleChange}
          value={formik.values.email}
        />

        <button type="submit">Submit</button>
      </form>
    </>
  );
};

export default Index;
